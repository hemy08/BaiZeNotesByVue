const emojiMenuChildrenStyle = {
    width: '400px',
    fontSize: '15pt',
    display: 'flex',
    flexDirection: 'row',
    flexWrap: 'wrap',
    alignItems: 'center'
}

const emojiMenuChildrenButtonStyle = {
    border: 'none',
    backgroundColor: 'transparent',
    fontSize: '20pt',
    width: '40px',
    height: '40px'
}

// åˆ›å»ºä¸€ä¸ªå‡½æ•°æ¥ç”Ÿæˆè¡¨æƒ…æŒ‰é’®çš„åˆ—è¡¨
function createEmojiButtons(
    emojiCategory: string,
    emojiStyle: Record<string, string>,
    buttonStyle: Record<string, string>
) {
    return h(
        'div',
        { style: emojiStyle },
        emojis[emojiCategory].data.map((text: string) => {
            return h(
                'button',
                {
                    style: buttonStyle,
                    onClick: () => {
                        EventBus.$emit('monaco-editor-insert-text', text)
                    }
                },
                text
            )
        })
    )
}

const emojiContextMenus = Object.keys(emojis).map((category) => {
    return {
        label: emojis[category].label, // æ ¹æ®ç±»åˆ«è®¾ç½®æ ‡ç­¾
        direction: 'lr',
        children: [
            {
                label: createEmojiButtons(
                    category,
                    emojiMenuChildrenStyle,
                    emojiMenuChildrenButtonStyle
                )
            }
        ]
    }
})

export function showEmojiQuickAccess(e: MouseEvent) {
    ContextMenu.showContextMenu({
        x: e.x,
        y: e.y + 10,
        items: emojiContextMenus as MenuItem[]
    })
}
